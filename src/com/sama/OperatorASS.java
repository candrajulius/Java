package com.sama;

public class OperatorASS {
    public static void main(String[] args){
//        operator assignments
//        Assignment operator penjumlahan
        int a = 20;
        a+=30;
        System.out.println("tampilkan hasil dari" + a);

//        operator pengurangan
        int b = 20;
        b-=100;
        System.out.println("hasil dari pengurangan = " + b);

//        operator pembagian
        int c = 300;
        c/=30;
        System.out.println("hasil dari pembagian = " + c);

//        operator assignment modulus
        int f = 20;
        f%=6;
        System.out.println("hasil dari modulus = " + f);
    }
}
