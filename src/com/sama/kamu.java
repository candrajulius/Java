package com.sama;

public class kamu {
    public static void main (String[] args){
//        Operasi aritmatika
        int variabel1 = 4;
        int variabel2 = 5;
        int hasil;
//        1 Penjumlhan
        hasil = variabel1 + variabel2;
        System.out.println(hasil);
//        2 Pengurangan
        hasil = variabel1 - variabel2;
        System.out.println(hasil);
//        3 Perkalian
        hasil = variabel1 * variabel2;
        System.out.println(hasil);
//        4 Pembagian
        hasil = variabel1 / variabel2;
        System.out.println(hasil);
        float b = 2;
        float a = 3;
        float hasilfloat = b/a;
        System.out.println(hasilfloat);
//        5 modulus
        hasil = variabel1 % variabel2;
        System.out.println(hasil);
    }
}
